from datasets import load_dataset
from transformers import AutoTokenizer, AutoModelForSequenceClassification
from torch.utils.data import DataLoader
from torch.optim import AdamW
import torch

# Load a tiny real dataset — emotion classification
dataset = load_dataset("emotion", split="train[:500]")  # just 500 samples to keep it fast
val_data = load_dataset("emotion", split="validation[:100]")

model_name = "distilbert-base-uncased"
tokenizer = AutoTokenizer.from_pretrained(model_name)

def tokenize(batch):
    return tokenizer(batch["text"], padding=True, truncation=True, max_length=64)

dataset = dataset.map(tokenize, batched=True)
val_data = val_data.map(tokenize, batched=True)

dataset.set_format("torch", columns=["input_ids", "attention_mask", "label"])
val_data.set_format("torch", columns=["input_ids", "attention_mask", "label"])

train_loader = DataLoader(dataset, batch_size=16, shuffle=True)
val_loader   = DataLoader(val_data, batch_size=16)

# 6 emotion classes: sadness, joy, love, anger, fear, surprise
model = AutoModelForSequenceClassification.from_pretrained(model_name, num_labels=6)
optimizer = AdamW(model.parameters(), lr=2e-5)

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model.to(device)

# ── TRAINING LOOP ──
for epoch in range(2):
    model.train()
    total_loss = 0
    for batch in train_loader:
        input_ids      = batch["input_ids"].to(device)
        attention_mask = batch["attention_mask"].to(device)
