from transformers import AutoTokenizer, AutoModelForSequenceClassification
from datasets import load_dataset
from torch.utils.data import DataLoader
from torch.optim import AdamW
import torch

dataset = load_dataset("emotion", split="train[:500]")
val_data = load_dataset("emotion", split="validation[:100]")

model_name = "distilbert-base-uncased"
tokenizer = AutoTokenizer.from_pretrained(model_name)

def tokenize(batch):
    return tokenizer(batch["text"], padding=True, truncation=True, max_length=64)

dataset = dataset.map(tokenize, batched=True)
val_data = val_data.map(tokenize, batched=True)
dataset.set_format("torch", columns=["input_ids", "attention_mask", "label"])
val_data.set_format("torch", columns=["input_ids", "attention_mask", "label"])

train_loader = DataLoader(dataset, batch_size=16, shuffle=True)
val_loader   = DataLoader(val_data, batch_size=16)

model = AutoModelForSequenceClassification.from_pretrained(model_name, num_labels=6)
optimizer = AdamW(model.parameters(), lr=2e-5)
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model.to(device)

emotion_labels = ["sadness", "joy", "love", "anger", "fear", "surprise"]

for epoch in range(2):
    model.train()
    total_loss = 0
    for batch_idx, batch in enumerate(train_loader):
        input_ids      = batch["input_ids"].to(device)
        attention_mask = batch["attention_mask"].to(device)
        labels         = batch["label"].to(device)

        outputs = model(input_ids=input_ids,
                        attention_mask=attention_mask,
                        labels=labels)
        loss = outputs.loss
        total_loss += loss.item()

        # 👇 THIS IS NEW — print first batch of each epoch so you SEE the learning
        if batch_idx == 0:
            probs = torch.softmax(outputs.logits[0].detach(), dim=-1)
            predicted = emotion_labels[probs.argmax()]
            actual    = emotion_labels[labels[0].item()]
            print(f"\nEpoch {epoch+1}, Batch 1 sample:")
            print(f"  Actual:    {actual}")
            print(f"  Predicted: {predicted}")
            print(f"  Confidence: {probs.max():.3f}")
            print(f"  All probs: { {emotion_labels[i]: f'{p:.3f}' for i,p in enumerate(probs)} }")
            print(f"  Batch loss: {loss.item():.4f}")

        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

    print(f"\nEpoch {epoch+1} complete | Avg Loss: {total_loss/len(train_loader):.4f}")

# Evaluation
model.eval()
correct, total = 0, 0
with torch.no_grad():
    for batch in val_loader:
        input_ids      = batch["input_ids"].to(device)
        attention_mask = batch["attention_mask"].to(device)
        labels         = batch["label"].to(device)
        outputs = model(input_ids=input_ids, attention_mask=attention_mask)
        preds   = outputs.logits.argmax(dim=-1)
        correct += (preds == labels).sum().item()
        total   += labels.size(0)

print(f"\nFinal Validation Accuracy: {correct/total:.2%}")


